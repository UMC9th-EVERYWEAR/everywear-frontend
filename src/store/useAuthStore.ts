import { create } from 'zustand';
import { persist } from 'zustand/middleware';

interface User {
	id: string;
	name: string;
	email: string;
	profileImage?: string;
}

interface AuthState {
	isLoggedIn: boolean;
	user: User | null;
	accessToken: string | null;
	login: (user: User, token: string) => void;
	logout: () => void;
}

export const useAuthStore = create<AuthState>()(
	persist(
		(set) => ({
			isLoggedIn: false,
			user: null,
			accessToken: null,
			login: (user, token) =>
				set({
					isLoggedIn: true,
					user,
					accessToken: token,
				}),
			logout: () =>
				set({
					isLoggedIn: false,
					user: null,
					accessToken: null,
				}),
		}),
		{
			name: 'auth-storage',
		}
	)
);